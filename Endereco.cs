class Endereco{
  private string rua, cidade, bairro, estado, pais;
  private int numero;
  public Endereco (string rua, string cidade, string bairro, string estado, string pais, int numero){
    this.rua = rua;
    this.cidade = cidade;
    this.bairro = bairro;
    this. estado = estado;
    this.pais = pais;
    this.numero = numero;
  }
}